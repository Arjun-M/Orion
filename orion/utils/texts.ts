export function pick(texts: string[], kwargs?: Record<string, string | number>): string {
  let msg = texts[Math.floor(Math.random() * texts.length)];
  if (kwargs) {
    for (const [key, val] of Object.entries(kwargs)) {
      msg = msg.replace(new RegExp(`\\{${key}\\}`, "g"), String(val));
    }
  }
  return msg;
}
